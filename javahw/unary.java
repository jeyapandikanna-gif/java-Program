class Unary
{
	public static void main(String []args)
	{
		int num=10;
		int a=num+=5;
		int b=num-=3;
		int c=num*=2;
		int d=num/=4;
		System.out.println("After +=5:"+a);
		System.out.println("After -=3:"+b);
		System.out.println("After *=2:"+c);
		System.out.println("After /=:"+d);
	}
}
		